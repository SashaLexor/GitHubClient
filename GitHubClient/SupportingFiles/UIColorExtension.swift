import UIKit

extension UIColor {
    public class var customBlue: UIColor
    {
        return UIColor(red: 66/255, green: 192/255, blue: 251/255, alpha: 1.0)
    }
    
    public class var customLightGray: UIColor
    {
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 0.8)
    }
}
